class KeyBoard
{
	String brand;
	String color;
	int noOfKeys;
	String type;
	double cost;
	KeyBoard()
	{
		System.out.println("Default constructor is created");
	}
	KeyBoard(String brand)
	{
		this.brand=brand;
	}
	KeyBoard(String brand,String color)
	{
		this.brand=brand;
		this.color=color;
	}
	KeyBoard(String brand,String color,int noOfKeys)
	{
		this.brand=brand;
		this.color=color;
		this.noOfKeys=noOfKeys;
	}
	KeyBoard(String brand,String color,int noOfKeys,String type)
	{
		this.brand=brand;
		this.color=color;
		this.noOfKeys=noOfKeys;
		this.type=type;
	}
	KeyBoard(String brand,String color,int noOfKeys,String type,double cost)
	{
		this.brand=brand;
		this.color=color;
		this.noOfKeys=noOfKeys;
		this.type=type;
		this.cost=cost;
	}
}

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
		
